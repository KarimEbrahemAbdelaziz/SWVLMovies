import Foundation
import UIKit

extension UIColor {
    
    open class var skyBlue: UIColor {
        get {
            return UIColor(red: 0.529, green: 0.737, blue: 0.914, alpha: 1.0)
        }
    }
    
    open class var semiBlue: UIColor {
        get {
            return UIColor(red: 0.278, green: 0.341, blue: 0.624, alpha: 1.0)
        }
    }
    
}
